def soma (n1,n2):
    soma=n1+n2
    return soma

def subtraçao(n1,n2):
    subtraçao=n1-n2
    return subtraçao

def divisao (n1,n2):
    divisao=n1/n2
    return divisao

def multiplicaçao(n1,n2):
    multiplicaçao=n1*n2
    return multiplicaçao

def calculadora(n1,n2):
    return soma(n1,n2), subtraçao(n1,n2), divisao(n1,n2), multiplicaçao(n1,n2)